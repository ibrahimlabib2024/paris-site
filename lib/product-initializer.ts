// Initialize products with robust error handling and data persistence
export function initializeProducts(): Product[] {
  if (typeof window === "undefined") {
    console.log(" Server-side: Returning default products")
    return [...defaultProducts]
  }

  try {
    // Always start with the complete default catalog
    console.log(" Initializing with complete product catalog:", defaultProducts.length)

    // Save to localStorage to ensure persistence
    localStorage.setItem("admin-products", JSON.stringify(defaultProducts))
    localStorage.setItem("products", JSON.stringify(defaultProducts))

    // Save metadata for debugging
    localStorage.setItem(
      "admin-products-meta",
      JSON.stringify({
        count: defaultProducts.length,
        lastUpdated: new Date().toISOString(),
        version: Date.now(),
        source: "complete-initialization",
      }),
    )

    return [...defaultProducts]
  } catch (error) {
    console.error(" Error initializing products:", error)
    // Always fallback to defaults
    return [...defaultProducts]
  }
}

